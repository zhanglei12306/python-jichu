# -*- coding: utf-8 -*-
"""
默认值：形参的值固定不变，我们可以在定义函数的时候为其指定默认值
需求：
    刘德华，北京欢迎你！您最想去哪里玩？
	杨幂，北京欢迎你！您最新想去哪里玩？
	小码哥，北京欢迎你！您最想去哪里玩？
"""

# 使用默认值
def welcome_beijing(name, question='您最想去哪里玩？'):
	"""
	调查问卷
	:param name: 名字
	:param question: 统一默认的问题
	:return:
	"""
	print(f'{name},北京欢迎你！{question}')


# 直接调用函数，忽略默认的形参
welcome_beijing('刘德华')

# 练习：输出杨幂和小码哥

'''
需求：询问
刘德华，北京欢迎你！您最想吃什么北京特产？
杨幂，北京欢迎你！您最想吃什么北京特产？
小码哥，北京欢迎你！您最想吃什么北京特产？

'''
# 修改默认的参数
welcome_beijing('刘德华', '您最想吃什么北京特产？')
